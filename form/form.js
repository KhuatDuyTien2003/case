function signIn(){
    let hide = document.getElementById('hide');
    let check = document.getElementById('check');
    let test = document.getElementById('h1');
    let test2 = document.getElementById('an');
    test.setAttribute("id","an");
    test2.setAttribute("id","h1");
    hide.setAttribute("id", "check");
    check.setAttribute("id", "hide");

}
